def potencia(base, expoente):
    """
    Calcula a potência de um número elevado a um expoente.

    Parâmetros:
    base : float ou int
        O número base que será elevado a uma potência.
    expoente : int
        O expoente ao qual a base será elevada. Pode ser positivo, zero ou negativo.

    Exemplo:
    potencia(2, 3)
    8
    """
    print(base ** expoente)
